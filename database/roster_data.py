def build_roster_rows(team_name):
    rows = []
    pitcher_index = 0
    hitter_index = 0
    team_offset = list(ROSTER_PLAYERS).index(team_name) % 4

    for name, position, age in ROSTER_PLAYERS[team_name]:
        if position == "P":
            pitcher_index += 1
            con = 84 - pitcher_index + team_offset
            power = 78 + ((pitcher_index + team_offset) % 5)
            eye = 72 + ((pitcher_index * 2 + team_offset) % 7)
            defense = 80 - pitcher_index * 2
            status = 1 if pitcher_index <= 5 else 0
            lineup_pos = 0
            role = ("선발", "선발", "선발", "필승조", "마무리", "추격조")[pitcher_index - 1]
        else:
            hitter_index += 1
            con = 68 + ((hitter_index * 3 + team_offset) % 17)
            power = 62 + ((hitter_index * 5 + team_offset) % 24)
            eye = 64 + ((hitter_index * 4 + team_offset) % 18)
            defense = 65 + ((hitter_index * 3 + team_offset) % 20)
            status = 1 if hitter_index <= 9 else 0
            lineup_pos = hitter_index if hitter_index <= 9 else 0
            role = "선수"

        salary = max(3000, (con + power + eye + defense - 240) * 1000)
        rows.append((team_name, name, position, age, con, power, eye, defense, status, lineup_pos, role, salary))

    return rows
